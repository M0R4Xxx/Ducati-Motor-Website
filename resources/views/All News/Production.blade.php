                Production Begins for Ducati’s <br class="hidden md:block" /> fifth-generation Monster
            </h1>
            <div class="ita-line w-40 mb-12"><div></div><div></div><div></div></div>

            <div class="relative w-full aspect-video mb-16 overflow-hidden bg-slate-200 dark:bg-slate-800 group cursor-crosshair">
                <img alt="Ducati Monster Production Line Ceremony" class="w-full h-full object-cover transition-transform duration-1000 group-hover:scale-110" src="https://images.ctfassets.net/x7j9qwvpvr5s/2Gqt9Ck4IdQCEKY7l0Uw0h/80ccd50661298ef3cca31bb4bf4462e5/11-27_SOP-Monster-editorial-img-wide-1330x768.jpg" />
