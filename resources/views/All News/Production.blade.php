            <div class="grid grid-cols-1 lg:grid-cols-12 gap-12 mb-20">
                <div class="lg:col-span-8 lg:col-start-3">
                    <div class="prose prose-lg dark:prose-invert max-w-none space-y-6 text-slate-700 dark:text-slate-300 leading-relaxed text-justify-custom">
                        <p class="text-xl font-medium text-slate-900 dark:text-white leading-snug">
                           Production has begun at the Borgo Panigale plant for the new <strong>Monster</strong>, the <strong>fifth generation</strong> of a true motorcycling icon. A bike born in 1992 from a brilliantly simple formula: “Everything you need, nothing more.” A concept that has united riders worldwide into a new family, the <strong>Monsteristi</strong>, who for over thirty years have continued to celebrate the spirit and identity of the Monster.
                        </p>
                        <p>
                            The new Monster, completely redesigned, <strong>combines lightness and technology</strong> with an unmistakable aesthetic. Its design reinterprets the style of the first Monster in a modern key, bringing back the signature elements that have always distinguished it: the <strong>headlight framed by the “shoulders”</strong> of the bison-back tank, the one-piece seat, and the short, lightweight tail section.
                        </p>
                        <p>
                           Thanks to its unique <strong>V-twin engine</strong>, the new Monster is slim, sporty, and characterized by very low running costs. The new <strong>V2 is lightweight and high-performing</strong>, thanks to <strong>IVT (Intake Variable Timing)</strong> distribution, which combines smoothness at low revs, punch at mid-range, and power at high revs: 80% of maximum torque is available from 4,000 to 10,000 rpm. The <strong>45,000 km valve clearance check interval</strong>, an absolute benchmark for the category, keeps maintenance costs very low.
                        </p>
                        <p>
                            The new Monster uses the engine as a structural element and adopts a <strong>monocoque frame</strong>, with a double-sided swingarm inspired by the Panigale V4 and a rear subframe in technopolymer and trellis. This technical layout, thanks to the lightness of the Ducati V2, <strong>reduces weight by 4 kg</strong> (175 kg dry, the lightest ever among four-valve models), improving agility and ease of handling. <strong>Showa suspension</strong> is tuned to ensure everyday comfort and sporty performance on twisty roads, confirming the dual soul of Ducati’s naked bike. The result is a motorcycle that is always stable, safe, and fun.
                        </p>
                        <p>
                            The 2026 Monster offers a <strong>more accessible and comfortable riding position</strong> thanks to a lower seat dibandingkan model sebelumnya (815 mm, yang dapat turun ke 775 mm dengan <em>low seat</em>) dan stang yang lebih tinggi dan lebih maju yang meningkatkan kontrol dan kenyamanan.
                        </p>
                        <p>
                            The <strong>four Riding Modes</strong> (Sport, Road, Urban, Wet) and the advanced electronics package with <strong>DTC, DWC, EBC, dan cornering ABS</strong> make it intuitive and versatile. Ergonomic handlebar controls simplify the management of electronic functions on a <strong>5” TFT display</strong>, ready for Ducati Multimedia System and Turn-by-turn navigation, with dual day/night mode and <strong>Road and Road Pro Info Modes</strong> that optimize readability in all conditions.
                        </p>
                        <p>
                            To inspire those who want to make the Monster truly their own, <strong>Ducati Performance</strong> offers numerous accessories to enhance its aesthetics, such as <strong>carbon fiber details</strong> or machined aluminum parts, as well as a sport seat with Alcantara upholstery. Sport enthusiasts will also appreciate homologated silencers with titanium sleeves and carbon end caps, developed in collaboration with <strong>Termignoni</strong>, which reduce weight (-0.2 kg) and unmistakably boost the Monster’s character.
                        </p>
                        <p>
                           The Monster will be available in dealerships starting <strong>February 2026</strong>, with other countries to follow, in two liveries: <strong>Ducati Red dan Iceberg White</strong>, in both Monster and Monster+ versions, equipped with a windshield and passenger seat cover. Both are also available in a <strong>35 kW version</strong> for riders with an A2 license.
                        </p>
